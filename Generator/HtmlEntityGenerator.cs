/// <summary>
/// 生成供 HtmlEntity 使用的前缀树数据。
/// </summary>
internal static class HtmlEntityGenerator
{
	/// <summary>
	/// 生成 HtmlEntity 的代码。
	/// </summary>
	/// <returns>HtmlEntity 的代码。</returns>
	/// <remarks>TextTemplating 模板需要 .NET Framework 4.5，没必要为了前缀树重新做一套实现，
	/// 需要更新的时候手工跑一下就行。</remarks>
	public static string Generate()
	{
		ReadOnlyPrefixTree<string> tree = new(Entities);
		var data = tree.GetData();

		StringBuilder code = new(@"using Cyjb.Collections;

namespace Cyjb.Markdown.Utils;

/// <summary>
/// 提供适用于 Markdown 的 HTML 的实体信息。
/// </summary>
internal static class HtmlEntity
{
	/// <summary>
	/// 前缀树的项。
	/// </summary>
	private static readonly KeyValuePair<string, string>[] items = new KeyValuePair<string, string>[]
	{
");
		foreach(var item in data.Items)
		{
			string value = item.Value.UnicodeEscape();
			if (value == "\"")
			{
				value = "\\\"";
			}
			code.Append($"		new(\"{item.Key}\", \"{value}\"),\r\n");
		}
		code.Append(@"	};
	/// <summary>
	/// 前缀树的节点。
	/// </summary>
	private static readonly int[] nodes = new int[]
	{
");
		foreach(var node in data.Nodes)
		{
			code.Append($"		{node},\r\n");
		}
		code.Append(@"	};
	/// <summary>
	/// 前缀树的转移列表。
	/// </summary>
	private static readonly int[] trans = new int[]
	{
");
		foreach(var tran in data.Trans)
		{
			code.Append($"		{tran},\r\n");
		}
		code.Append(@"	};
	/// <summary>
	/// HTML 实体前缀树，最后会以 ; 结尾。
	/// </summary>
	public static readonly ReadOnlyPrefixTree<string> Entities = new(
		new ReadOnlyPrefixTreeData<string>(items, nodes, trans));
}

");
		return code.ToString();
	}
